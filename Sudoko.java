    private int[][] grid_to_format() {
        int[][] sol = new int[9][9];
        for (int i = 0; i < 9; i++) {
            for (int j = 0; j < 9; j++) {
                String str = grid[i][j].getText();
                if (str.length() == 1) {
                    sol[i][j] = Integer.parseInt(str);
                } else {
                    sol[i][j] = -1;
                }
            }
        }
        return sol;
    }

